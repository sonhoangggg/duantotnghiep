<script>
const heroSlides = document.querySelectorAll('.hero-slide');
const heroDotsWrap = document.querySelector('.hero-dots');
let heroIndex = 0;

heroSlides.forEach((_, i) => {
    const dot = document.createElement('span');
    if (i === 0) dot.classList.add('active');
    dot.addEventListener('click', () => showHeroSlide(i));
    heroDotsWrap.appendChild(dot);
});

const heroDots = document.querySelectorAll('.hero-dots span');

function showHeroSlide(index) {
    heroSlides[heroIndex].classList.remove('active');
    heroDots[heroIndex].classList.remove('active');

    heroIndex = index;

    heroSlides[heroIndex].classList.add('active');
    heroDots[heroIndex].classList.add('active');
}

document.querySelector('.hero-next').onclick = () =>
    showHeroSlide((heroIndex + 1) % heroSlides.length);

document.querySelector('.hero-prev').onclick = () =>
    showHeroSlide((heroIndex - 1 + heroSlides.length) % heroSlides.length);

setInterval(() => {
    showHeroSlide((heroIndex + 1) % heroSlides.length);
}, 4000);
</script>
